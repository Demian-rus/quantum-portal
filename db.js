const DB = (function() {
  const processedDB = {};
  const allWordsSet = new Set();
  let duplicatesCount = 0;

  console.groupCollapsed("🛠 DB Integrity Check");

  for (const subject in RAW_DB) {
    processedDB[subject] = {};
    for (const level in RAW_DB[subject]) {
      processedDB[subject][level] = [];
      
      RAW_DB[subject][level].forEach(item => {
        // 1. Валидация формата
        if (!item.w || !item.q) {
          console.error(`❌ Invalid item in ${subject}/${level}:`, item);
          return;
        }

        // 2. Нормализация (Ё->Е, trim, upper)
        const word = item.w.trim().toUpperCase().replace(/Ё/g, "Е");
        const question = item.q.trim();

        // 3. Проверка на дубликаты (глобальная по всей игре)
        if (allWordsSet.has(word)) {
          console.warn(`⚠️ Duplicate word ignored: "${word}" (${subject}/${level})`);
          duplicatesCount++;
          return;
        }

        allWordsSet.add(word);
        processedDB[subject][level].push({ w: word, q: question });
      });

      // 4. Сортировка по алфавиту
      processedDB[subject][level].sort((a, b) => a.w.localeCompare(b.w));
    }
  }

  console.log(`✅ DB Loaded. Total unique words: ${allWordsSet.size}`);
  if (duplicatesCount > 0) console.log(`🗑 Removed ${duplicatesCount} duplicates`);
  console.groupEnd();

  return processedDB;
})();

// API для добавления слов из консоли (для учителей)
DB.add = function(subject, level, word, clue) {
  if (!DB[subject]) DB[subject] = {};
  if (!DB[subject][level]) DB[subject][level] = [];
  
  const normWord = word.trim().toUpperCase().replace(/Ё/g, "Е");
  
  // Проверка дублей
  for (const s in DB) {
    for (const l in DB[s]) {
      if (DB[s][l].find(x => x.w === normWord)) {
        console.warn("Такое слово уже есть!");
        return;
      }
    }
  }

  DB[subject][level].push({ w: normWord, q: clue });
  DB[subject][level].sort((a, b) => a.w.localeCompare(b.w));
  console.log(`✅ Добавлено: ${normWord}`);
};